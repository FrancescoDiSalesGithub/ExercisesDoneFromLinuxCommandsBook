package question5;



public class SquareAndCube 
{


    public static void main(String args[])
    {

        for(int i=0;i<11;i++)
        {
            int square = i*i;
            int cube = i*i*i;

            System.out.println("number: "+i);
            System.out.println("square: "+square);
            System.out.println("cube: "+cube);
        }

    }

    
}
